for index, slide_data in enumerate(slides):
    layout = prs.slide_layouts[1 if index else 0]
    slide = prs.slides.add_slide(layout)
    slide.shapes.title.text = slide_data["title"]

    for placeholder in slide.placeholders:
        if placeholder.is_placeholder and placeholder.placeholder_format.type == 1:
            tf = placeholder.text_frame
            tf.clear()
            first = tf.add_paragraph()
            first.text = slide_data["body"][0]
            first.font.size = Pt(20 if index else 18)
            for paragraph in slide_data["body"][1:]:
                p = tf.add_paragraph()
                p.text = paragraph
                p.font.size = Pt(20 if index else 18)
            break

